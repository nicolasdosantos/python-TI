class Instrumento:
    def tocar(self):
        print("Tocando")

class Violino(Instrumento):
    def tocar(self):
        print("Violino esta tocando")

class Piano(Instrumento):
    def tocar(self):
        print("Piano esta tocando")

class Flauta(Instrumento):
    def tocar(self):
        print("Flauta esta tocando")